//let is the keyword that is used as a data type in JS, it is used to create variables of anydata type 
//Since after creating a variable of one data type, we can update it to another data type so JS is called dynamically typed language
let i = 10;
console.log(i);         //Here 10 will be printed

// updating the value of i from int to string
i = "Hello World";
console.log(i);         //Now here the value of i will be printed Hello World as in the 7th line it has been updated to Hello World